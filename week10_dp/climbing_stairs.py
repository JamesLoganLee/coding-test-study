# [DP] 계단 오르기 - 정상까지 가는 방법의 수 구하기
# 계단이 n개 있고, 한 번에 1칸 또는 2칸씩 오를 수 있을 때
# 정상까지 오르는 방법의 수를 구하기
# 예: n=5 -> 8가지 / n=10 -> 89가지
#
# ═════════════════════════════════════════════════════════════
# [DP(동적계획법)란]
#   핵심 아이디어: "한 번 계산한 건 메모해두고, 다음엔 다시 계산하지
#   않고 그냥 꺼내 쓴다." (메모이제이션)
#   이름이 어렵지만 그냥 '미리 구한 답을 표에 저장해두고 활용하는
#   방법' 정도로 이해하면 충분하다.
#
# [작은 경우부터 손으로 세어보기]
#   계단 1개: 1가지 (1칸)
#   계단 2개: 2가지 (1+1, 2)
#   계단 3개: 3가지 (1+1+1, 1+2, 2+1)
#   계단 4개: 5가지 (1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2)
#   -> 규칙: 1, 2, 3, 5 ... 각 계단 방법 수 = 바로 전 계단 + 그전전 계단
#
# [점화식 - 왜 이런 규칙이 나오나]
#   dp[n] = dp[n-1] + dp[n-2]
#   정상(n칸)에 도착하는 '마지막 한 걸음'은 딱 두 가지뿐이다:
#     - 1칸을 올라서 도착 -> 그 전엔 (n-1)칸에 있었다 -> 방법의 수 = dp[n-1]
#     - 2칸을 올라서 도착 -> 그 전엔 (n-2)칸에 있었다 -> 방법의 수 = dp[n-2]
#   이 두 경우를 더하면 전체 방법의 수! (피보나치 수열과 동일한 패턴)
#
# [새 문법]
#   [0] * (n+1)  : 0이 (n+1)개인 리스트 만들기. 예) [0]*5 -> [0,0,0,0,0]
#                  '0번부터 n번까지 담을 빈 표'를 미리 만들어두는 것.
#                  (0번 인덱스는 실제로 안 쓰지만 자리를 맞추려고 만듦)
#
# [코드 흐름]
#   1) 빈 표(dp) 준비
#   2) 기본값(dp[1], dp[2])은 손으로 셀 수 있으니 미리 넣기
#   3) 3번째 계단부터 목표(n)까지, '바로 앞 두 개를 더해서' 표를 채워나가기
#   4) 이미 구해둔 값(dp[i-1], dp[i-2])을 재사용 -> 이게 DP의 본질!
#      (다시 계산 안 하고 표에서 꺼내 쓰기 때문에 빠르다)
#
# [표 채우기 과정] n=5
#   i=3: dp[2]+dp[1] = 2+1 = 3   -> dp=[0,1,2,3,0,0]
#   i=4: dp[3]+dp[2] = 3+2 = 5   -> dp=[0,1,2,3,5,0]
#   i=5: dp[4]+dp[3] = 5+3 = 8   -> dp=[0,1,2,3,5,8]
#   -> dp[5] = 8 (정답)
# ═════════════════════════════════════════════════════════════

def solution(n):
    dp = [0] * (n + 1)         # 0번부터 n번까지 담을 빈 표 만들기
    dp[1] = 1                    # 계단 1개일 때는 1가지 (기본값)
    dp[2] = 2                    # 계단 2개일 때는 2가지 (기본값)

    for i in range(3, n + 1):        # 3번째 계단부터 n번째까지 채우기
        dp[i] = dp[i-1] + dp[i-2]     # 바로 앞 두 개를 더해서 저장 (재사용!)

    return dp[n]

# 테스트
print(solution(5))     # 8
print(solution(10))    # 89
