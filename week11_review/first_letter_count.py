# [복습] 단어들의 첫 글자 개수 세기
# 여러 주차 개념 통합: 6주차(문자열 인덱싱) + 7주차(딕셔너리 개수세기)
# 예: ["apple","banana","avocado","berry","cherry"] -> {'a':2,'b':2,'c':1}
#
# ─────────────────────────────────────────────────────────
# [복습 포인트] "새 문제도 알고보면 이미 배운 도구의 조합"
#   6주차: word[0] - 문자열도 리스트처럼 위치로 글자를 꺼낼 수 있다
#          (회문 판별에서 word[left] 썼던 것과 동일한 원리)
#   7주차: "있으면 +1, 없으면 1로 시작" 개수 세기 패턴
#          (글자 개수 세기에서 썼던 것과 완전히 동일)
#
# [차이점] 7주차는 '글자 하나하나'를 셌지만, 이번엔 '단어의 첫 글자만' 센다.
#          -> word[0]으로 첫 글자만 뽑아낸 뒤, 그 글자를 세는 것뿐.
# ─────────────────────────────────────────────────────────

def solution(words):
    count = {}                    # 빈 딕셔너리
    for word in words:             # 단어를 하나씩 꺼내며
        first = word[0]             # 그 단어의 첫 글자 꺼내기 (6주차)
        if first in count:           # 이미 나온 첫 글자면
            count[first] += 1          # 개수 +1
        else:                          # 처음 나온 첫 글자면
            count[first] = 1            # 개수를 1로 시작
    return count                  # (7주차 개수세기 패턴 그대로)

# 테스트
print(solution(["apple", "banana", "avocado", "berry", "cherry"]))
# {'a': 2, 'b': 2, 'c': 1}
