# [복습] 짝수만 골라 정렬 후 가장 큰 2개의 합
# 여러 주차 개념 통합: 3주차(골라담기) + 4주차(정렬) + 5주차(그리디)
# 예: [3,8,1,6,2,9,4] -> 짝수만[8,6,2,4] -> 정렬[2,4,6,8] -> 큰2개 8+6 = 14
#
# ─────────────────────────────────────────────────────────
# [복습 포인트] "새 문제 = 배운 패턴들의 조합"
#   3주차: 빈 리스트 -> 반복 -> 조건 -> append (골라 담기)
#   4주차: sorted(x, reverse=True) (내림차순 정렬)
#   5주차: 가장 큰 두 값 곱/합 하기 (그리디 - 정렬 후 앞쪽 값 활용)
#
# [처음 작성 → 다듬은 과정 - 실전 감각]
#   처음엔 반복문 '안에서' 매번 sorted()를 호출하는 불필요한 줄이 있었다.
#   -> 어차피 반복문이 다 끝난 뒤 한 번만 정렬하면 되므로,
#      반복문 밖으로 빼서 계산 낭비를 없앴다.
#   -> 변수(answer)는 '마지막에 대입한 값'만 최종적으로 남는다(2주차 개념).
#      중간에 여러 번 대입해도 마지막 대입이 결과를 결정한다.
# ─────────────────────────────────────────────────────────

def solution(numbers):
    result = []
    for x in numbers:
        if x % 2 == 0:           # 짝수만
            result.append(x)       # 골라 담기 (3주차)
    result = sorted(result, reverse=True)   # 큰 것부터 정렬 (4주차)
    answer = result[0] + result[1]          # 가장 큰 두 개의 합 (5주차)
    return answer

# 테스트
print(solution([3, 8, 1, 6, 2, 9, 4]))   # 14
