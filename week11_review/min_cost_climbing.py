# [복습·응용] 계단 오르기 최소 비용 (10주차 DP의 심화 버전)
# 각 계단마다 비용이 있고, 1칸 또는 2칸씩 올라 정상까지 가는 최소 비용 구하기
# 예: costs=[10,15,20] -> 15 (1번 계단에서 2칸 뛰어 2번을 건너뛰고 바로 정상)
#
# ─────────────────────────────────────────────────────────
# [10주차 계단 오르기와 다른 점]
#   10주차: dp[i] = dp[i-1] + dp[i-2]              (방법의 '개수'를 더함)
#   이번문제: dp[i] = min(dp[i-1], dp[i-2]) + costs[i]  (더 싼 '경로'를 선택 + 비용 추가)
#   -> "더하기" 대신 "min()으로 더 싼 쪽 선택"이 핵심 차이.
#
# [dp[i]의 의미] "i번 계단에 도착하기까지 드는 최소 비용"
#
# [기본값]
#   dp[0] = costs[0]   : 0번 계단은 시작점 -> 그 계단 비용만 냄
#   dp[1] = costs[1]   : 1번 계단도 시작점 -> 그 계단 비용만 냄
#
# [점화식 - i번 계단에 도착하는 두 가지 방법 중 더 싼 쪽]
#   dp[i] = min(dp[i-1], dp[i-2]) + costs[i]
#   - 방법A: 1칸 전(i-1)에서 1칸 올라오기
#   - 방법B: 2칸 전(i-2)에서 2칸 올라오기
#   - 둘 중 더 싼 쪽을 고르고, 지금 계단(i) 비용을 더한다 (이 계단은 반드시 밟으므로)
#
# [실행 흐름] costs=[10,15,20]
#   dp[0]=10, dp[1]=15
#   i=2: min(dp[1],dp[0]) + costs[2] = min(15,10) + 20 = 10+20 = 30
#   dp = [10, 15, 30]
#
# [★ 핵심 트릭] 왜 return이 dp[n-1]이 아니라 min(dp[n-1], dp[n-2])인가
#   정상은 '마지막 계단 다음'에 있다. 정상 가는 방법도 두 가지:
#     - 방법A: 마지막 계단(dp[n-1]=dp[2]=30)에 도착 후 1칸 더 올라 정상
#     - 방법B: 마지막 전 계단(dp[n-2]=dp[1]=15)에서 2칸 뛰어 마지막 계단을
#              아예 건너뛰고 바로 정상 도착!
#   -> 방법B가 더 싸면, 비싼 마지막 계단을 밟지 않아도 된다.
#   -> min(dp[2], dp[1]) = min(30, 15) = 15  (정답)
# ─────────────────────────────────────────────────────────

def solution(costs):
    n = len(costs)
    dp = [0] * n
    dp[0] = costs[0]              # 0번에서 시작하는 비용
    dp[1] = costs[1]              # 1번에서 시작하는 비용

    for i in range(2, n):
        dp[i] = min(dp[i-1], dp[i-2]) + costs[i]   # 더 싼 경로 + 지금 계단 비용

    return min(dp[n-1], dp[n-2])   # 마지막 또는 마지막 전 계단에서 정상 도착 (더 싼 쪽)

# 테스트
print(solution([10, 15, 20]))                              # 15
print(solution([1, 100, 1, 1, 1, 100, 1, 1, 100, 1]))       # 6
