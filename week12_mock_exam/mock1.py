# 문제3. 소수(prime number) 판별하기
#   소수 = 1과 자기 자신으로만 나누어떨어지는 수 (2,3,5,7,11...)
#   예: n=7 -> True (1과 7로만 나눠짐) / n=8 -> True가 아님(2,4로도 나눠짐) -> False
#
# [엣지 케이스(예외) 처리 - 2주차에서 배운 감각의 재사용]
#   if n <= 1:
#       return False
#   -> 소수의 정의는 '2 이상의 자연수' 중에서만 따진다.
#      0, 1, 음수는 소수가 아니므로 이런 값이 들어오면 바로 False.
#      이걸 안 챙기면 아래 반복문에서 이상하게 동작하거나 틀린 답이 나올 수 있음.
#
# [핵심 아이디어] n이 소수인지 확인하려면?
#   2부터 (n-1)까지 하나하나 나눠봐서, 나누어떨어지는 게 하나라도 있으면
#   소수가 아니다(False). 끝까지 나누어떨어지는 게 없으면 소수(True).
#
# [★ 최적화 - 왜 n-1까지가 아니라 제곱근까지만 확인해도 되는가]
#   for i in range(2, int(n**0.5) + 1):
#
#   예를 들어 100이 소수인지 확인한다고 하자.
#   100 = 2 x 50 = 4 x 25 = 5 x 20 = 10 x 10 = 20 x 5 = 50 x 2 ...
#   자세히 보면, 약수는 항상 '짝'을 이루어 나타난다.
#   그리고 그 짝 중 하나는 반드시 '제곱근(10) 이하'에 있다.
#   (10 x 10 = 100 이 딱 중간, 그 이후로는 앞서 나온 짝의 반대편일 뿐)
#   -> 그래서 1부터 제곱근까지만 확인해도, 있는 약수는 다 걸러낼 수 있다.
#   -> 100까지 다 안 나눠봐도, 10까지만 나눠보면 충분! (계산량이 크게 줄어듦)
#
#   n ** 0.5   : n의 제곱근을 구하는 방법 (예: 100 ** 0.5 = 10.0)
#   int(...)   : 소수점을 버리고 정수로 바꿈 (10.0 -> 10)
#   + 1        : range는 끝值를 포함 안 하므로, 제곱근 값 자체도 확인하려면 +1 필요
#
# [한 줄씩 풀이]
#   for i in range(2, int(n**0.5)+1):  : 2부터 제곱근까지 하나씩 i에 꺼냄
#       if n % i == 0:                   : n이 i로 나누어떨어지면(나머지 0)
#           return False                    : 약수를 찾은 것 -> 소수 아님, 바로 종료
#   return True                        : 끝까지 나누어떨어지는 게 없었다 -> 소수!
#
# [숫자로 확인해보기]
#   n=7:  range(2, int(7**0.5)+1) = range(2, 3) = [2]
#         7%2=1(아님) -> 반복 끝 -> return True (정답)
#   n=8:  range(2, int(8**0.5)+1) = range(2, 3) = [2]
#         8%2=0(나눠짐!) -> return False (정답)
# ─────────────────────────────────────────────────────────

def solution3(n):
    if n <= 1:
        return False
    for i in range(2, int(n**0.5) + 1):
        if n % i == 0:
            return False
    return True


