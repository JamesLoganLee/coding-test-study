# ─────────────────────────────────────────────────────────
# 문제1. 3의 배수만 골라 모두 더한 값 구하기
#   예: [1, 3, 6, 7, 9, 10] -> 18 (3+6+9)
#
# [사용한 개념]
#   1주차: answer = 0 에서 시작해 += 로 계속 쌓아가는 '누적' 패턴
#   3주차: for + if 조합으로 '조건에 맞는 것만' 골라내는 필터링 패턴
#
# [한 줄씩 풀이]
#   answer = 0            : 합계를 담을 그릇을 0으로 시작 (아직 아무것도 안 더함)
#   for num in numbers:    : 리스트의 값을 하나씩 num에 꺼내옴
#       if num % 3 == 0:     : num을 3으로 나눈 나머지가 0이면 = 3의 배수라는 뜻
#           answer += num       : 3의 배수일 때만 answer에 더해줌 (배수가 아니면 그냥 넘어감)
#   return answer          : 반복이 다 끝난 뒤, 쌓인 합계를 돌려줌
#
# [숫자로 직접 따라가기] numbers = [1, 3, 6, 7, 9, 10]
#   num=1  -> 1%3=1 (배수 아님) -> 건너뜀            answer=0
#   num=3  -> 3%3=0 (배수!)     -> answer += 3        answer=3
#   num=6  -> 6%3=0 (배수!)     -> answer += 6        answer=9
#   num=7  -> 7%3=1 (배수 아님) -> 건너뜀            answer=9
#   num=9  -> 9%3=0 (배수!)     -> answer += 9        answer=18
#   num=10 -> 10%3=1(배수 아님) -> 건너뜀            answer=18
#   -> 최종 answer = 18 (정답)
# ─────────────────────────────────────────────────────────

def solution1(numbers):
    answer = 0
    for num in numbers:
        if num % 3 == 0:
            answer += num
    return answer


