# 문제2. 문자열에서 가장 많이 등장한 글자 찾기
#   예: "banana" -> "a" (a가 3번으로 최다: b1, a3, n2)
#
# [사용한 개념]
#   7주차에서 배운 "있으면+1, 없으면1로 시작" 패턴을 직접 짤 수도 있지만,
#   이번엔 그 패턴을 '자동으로' 해주는 파이썬 내장 도구를 스스로 찾아 사용함.
#
# [새로 사용한 도구 - collections.Counter]
#   from collections import Counter
#   -> 'Counter'는 리스트나 문자열 안의 값들이 각각 몇 번 나왔는지
#      자동으로 세어서 특별한 딕셔너리 형태로 만들어주는 도구.
#
#   count = Counter("banana")
#   -> 결과: Counter({'a': 3, 'n': 2, 'b': 1})
#      (7주차에 직접 만들었던 {'a':3,'n':2,'b':1} 딕셔너리와 사실상 같은 내용,
#       Counter가 이 세는 과정을 대신 자동으로 해준 것)
#
#   count.most_common(1)
#   -> "가장 많이 나온 것 1개를 순위表 형태로 줘" 라는 뜻.
#   -> 결과: [('a', 3)]  <- (글자, 개수) 짝을 담은 리스트가 나옴
#      most_common(2)라고 하면 상위 2개, most_common(3)이면 상위 3개도 가능.
#
#   count.most_common(1)[0]
#   -> 리스트에서 0번째(첫 번째) 항목을 꺼냄 -> ('a', 3)  (튜플 형태)
#
#   count.most_common(1)[0][0]
#   -> 그 튜플에서 0번째(글자만) 꺼냄 -> 'a'
#      (튜플도 리스트처럼 [0], [1]로 위치 접근 가능. [0]=글자, [1]=개수)
#
# [정리하면 한 줄로]
#   count.most_common(1)[0][0]
#   = "가장 많이 나온 것 1개를 뽑아서" -> "그 중 글자 부분만 꺼내기"
# ─────────────────────────────────────────────────────────

from collections import Counter

def solution2(s):
    count = Counter(s)
    return count.most_common(1)[0][0]


