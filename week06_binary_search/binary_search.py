# [이분탐색] 정렬된 리스트에서 값 찾기
# 정렬된 numbers에서 target의 위치(인덱스)를 찾아 return. 없으면 -1
# 예: [1,3,5,7,9]에서 7 찾기 -> 3 (0번부터 세면 3번째)
#
# ─────────────────────────────────────────────────────────
# [이분탐색이란]
#   두꺼운 사전에서 단어를 찾을 때, 앞에서부터 한 장씩 넘기지 않고
#   '가운데를 펴서 → 앞/뒤 절반만 다시 보기'를 반복하는 것과 같다.
#   매번 절반씩 버리므로 아주 빠르다.
#   (100만 개 데이터도 약 20번이면 찾음. 하나씩 보면 최대 100만 번)
#
# [전제조건 ★]
#   반드시 '정렬'되어 있어야 한다!
#   정렬돼 있어야 "가운데보다 크면 오른쪽에 있다"는 판단이 가능하다.
#
# [핵심 3줄]
#   mid = (left + right) // 2   : 가운데 위치 계산
#   left = mid + 1              : 왼쪽 절반 버리기 (오른쪽만 보기)
#   right = mid - 1             : 오른쪽 절반 버리기 (왼쪽만 보기)
#
# [실제 흐름] numbers=[1,3,5,7,9], target=7
#   1회차: left=0, right=4 → mid=2 → numbers[2]=5 → 5<7이니 오른쪽!
#          → left=3 (1,3,5를 통째로 버림)
#   2회차: left=3, right=4 → mid=3 → numbers[3]=7 → 찾았다! return 3
#   → 단 2번만에 찾음
#
# [새 문법]
#   while 조건:  → 조건이 참인 동안 계속 반복 (for와 달리 횟수 미정)
#   elif        → "else if". 조건을 여러 개 차례로 확인할 때
# ─────────────────────────────────────────────────────────

def solution(numbers, target):
    left = 0                          # 탐색 범위 왼쪽 끝
    right = len(numbers) - 1          # 탐색 범위 오른쪽 끝

    while left <= right:              # 범위가 남아있는 동안 반복
        mid = (left + right) // 2     # 가운데 위치
        if numbers[mid] == target:    # 가운데가 찾는 값이면
            return mid                # 바로 반환!
        elif numbers[mid] < target:   # 가운데가 더 작으면 → 찾는 값은 오른쪽에
            left = mid + 1            # 왼쪽 절반 버림
        else:                         # 가운데가 더 크면 → 찾는 값은 왼쪽에
            right = mid - 1           # 오른쪽 절반 버림

    return -1                         # 다 찾아봐도 없으면 -1

# 테스트
print(solution([1, 3, 5, 7, 9], 7))   # 3
print(solution([1, 3, 5, 7, 9], 4))   # -1 (없는 값)
