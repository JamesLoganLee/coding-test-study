# [투 포인터] 두 수의 합이 target이 되는 조합 찾기
# 정렬된 numbers에서 두 수를 더해 target이 되면 True, 없으면 False
# 예: [1,3,5,7,9], target=12 -> True (3+9=12)
#
# ─────────────────────────────────────────────────────────
# [투 포인터란]
#   양쪽 끝에서 두 사람(손가락)이 서로를 향해 걸어오는 방식.
#   왼쪽(left)과 오른쪽(right)에 손가락을 두고,
#   상황에 따라 한쪽씩 안으로 옮기며 답을 찾는다.
#
# [이분탐색과의 차이]
#   이분탐색 : 가운데를 보고 '절반을 버림'   → 하나의 값 찾기
#   투 포인터 : 양 끝 두 개를 보고 한쪽을 옮김 → 두 값의 '조합' 찾기
#   (둘 다 '정렬된' 리스트에서 사용)
#
# [움직이는 논리 ★] 정렬되어 있으니 이런 판단이 가능하다
#   합이 작다 → 더 큰 값이 필요 → left를 오른쪽으로 (오른쪽일수록 큰 값)
#   합이 크다 → 더 작은 값이 필요 → right를 왼쪽으로 (왼쪽일수록 작은 값)
#
# [실제 흐름] numbers=[1,3,5,7,9], target=12
#   1회차: left=0(값1), right=4(값9) → 합=10 → 10<12(작다) → left를 1로
#   2회차: left=1(값3), right=4(값9) → 합=12 → 딱 맞다! → return True
# ─────────────────────────────────────────────────────────

def solution(numbers, target):
    left = 0                                    # 왼쪽 손가락 (맨 앞)
    right = len(numbers) - 1                    # 오른쪽 손가락 (맨 뒤)

    while left < right:                         # 두 손가락이 만나기 전까지
        total = numbers[left] + numbers[right]  # 두 값의 합
        if total == target:                     # 합이 딱 맞으면
            return True                         # 찾았다!
        elif total < target:                    # 합이 작으면 → 더 큰 수 필요
            left += 1                           # 왼쪽 손가락을 오른쪽으로
        else:                                   # 합이 크면 → 더 작은 수 필요
            right -= 1                          # 오른쪽 손가락을 왼쪽으로

    return False                                # 다 봐도 없으면 False

# 테스트
print(solution([1, 3, 5, 7, 9], 12))   # True  (3+9)
print(solution([1, 3, 5, 7, 9], 100))  # False (불가능)
