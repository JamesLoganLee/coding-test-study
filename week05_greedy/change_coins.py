# [그리디] 거스름돈 최소 개수
# 거스름돈 money를 500/100/50/10원으로 낼 때 필요한 최소 동전 개수
# 예: 1260원 -> 500x2 + 100x2 + 50x1 + 10x1 = 6개
#
# [그리디 포인트] "큰 단위부터 최대한 많이" 쓰면 개수가 최소가 된다.
#
# [핵심 문법]
#   //  : 몫   (1260 // 500 = 2)   -> 이 단위로 몇 개 낼 수 있는지
#   %   : 나머지 (1260 % 500 = 260) -> 쓰고 남은 돈
#   money = money % coin -> 남은 돈으로 '갱신'해서 다음 단위 계산에 사용

def solution(money):
    count = 0
    coins = [500, 100, 50, 10]      # 큰 단위부터!
    for coin in coins:
        count += money // coin      # 이 단위로 낼 수 있는 개수 더하기
        money = money % coin        # 쓰고 남은 돈으로 갱신
    return count

# 테스트
print(solution(1260))   # 6
