# [스택] 괄호 짝 검사
# 괄호 문자열이 짝이 맞는지 확인 ( '(' 와 ')' 만 있다고 가정)
# 예: "(())" -> True (짝 맞음) / "(()" -> False (여는 괄호 하나가 안 닫힘)
#
# ─────────────────────────────────────────────────────────
# [스택(Stack)이란]
#   접시 쌓기와 같다. 쌓으면(넣으면) 맨 위에 쌓이고,
#   뺄 때도 '맨 위(마지막에 넣은 것)부터' 뺀다. 아래 접시를 먼저 못 뺀다.
#   -> LIFO (Last In, First Out) = 마지막에 넣은 게 먼저 나온다.
#
#   append(x) : 넣기(쌓기)
#   pop()     : 맨 뒤(마지막) 값을 꺼내면서 제거
#
# [이 문제에서 스택을 쓰는 이유]
#   괄호는 '가장 최근에 연 것부터 닫혀야' 짝이 맞는다. (안쪽부터 닫힘)
#   그래서 여는 괄호를 스택에 쌓아두고, 닫을 때마다 가장 최근 것과 짝을 맞춘다.
#
# [실제 흐름] "(())"
#   '(' -> 쌓기 -> [(]
#   '(' -> 쌓기 -> [(, (]
#   ')' -> 짝 찾아 꺼내기 -> [(]
#   ')' -> 짝 찾아 꺼내기 -> []   (다 끝나고 스택이 비어있음 -> True)
# ─────────────────────────────────────────────────────────

def solution(s):
    stack = []
    for ch in s:                # 문자열의 글자를 하나씩(ch = character)
        if ch == '(':             # 여는 괄호면
            stack.append(ch)       # 스택에 쌓기
        else:                      # 닫는 괄호면
            if not stack:            # 스택이 비어있으면(짝 없이 닫으려 함)
                return False           # 잘못된 괄호!
            stack.pop()               # 짝을 찾았으니 하나 꺼내기(제거)
    return len(stack) == 0      # 다 끝났을 때 스택이 비어있어야 완벽히 짝맞음

# 테스트
print(solution("(())"))   # True
print(solution("(()"))    # False (여는 괄호 하나가 안 닫힘)
print(solution("())"))    # False (닫는 괄호가 더 많음)
