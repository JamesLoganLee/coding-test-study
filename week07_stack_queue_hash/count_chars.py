# [해시/딕셔너리] 글자 개수 세기
# 문자열 s에서 각 글자가 몇 번씩 나오는지 딕셔너리로 return
# 예: "hello" -> {'h':1, 'e':1, 'l':2, 'o':1}
#
# ─────────────────────────────────────────────────────────
# [딕셔너리(dict/해시)란]
#   국어사전과 같다. '단어(키)'를 찾으면 그 '뜻(값)'이 바로 나온다.
#   전체를 뒤지지 않고 키만 알면 바로 값을 찾는다 (리스트보다 빠름).
#
#   리스트   : numbers[0] 처럼 '번호(위치)'로 찾음
#   딕셔너리 : scores["철수"] 처럼 '이름(키)'으로 찾음
#
#   count = {}          : 빈 딕셔너리 만들기
#   count["사과"] = 3    : "사과"라는 키에 3을 저장
#   ch in count          : ch가 이미 키로 있는지 확인 (있으면 True)
#   count[ch] += 1       : 있는 키의 값을 1 증가
#   count[ch] = 1        : 새 키를 만들며 값 1로 시작
#
# [개수 세기의 정석 패턴] "있으면 +1, 없으면 1로 시작"
#   if ch in count:
#       count[ch] += 1     # 이미 나온 글자면 개수 추가
#   else:
#       count[ch] = 1      # 처음 나온 글자면 1로 시작
#
# [실제 흐름] "hello"
#   h -> 처음 -> {'h':1}
#   e -> 처음 -> {'h':1,'e':1}
#   l -> 처음 -> {'h':1,'e':1,'l':1}
#   l -> 또 나옴! count['l']+=1 -> {'h':1,'e':1,'l':2}
#   o -> 처음 -> {'h':1,'e':1,'l':2,'o':1}
#
# [★ 들여쓰기 주의 - 자주 하는 실수]
#   return count를 for 블록 '안'(if/else와 같은 들여쓰기)에 두면,
#   글자 하나 처리하자마자 함수가 끝나버려 첫 글자 결과만 나온다.
#   반드시 return은 for와 같은 들여쓰기(= for 블록이 다 끝난 뒤)에 둘 것!
# ─────────────────────────────────────────────────────────

def solution(s):
    count = {}                  # 빈 딕셔너리
    for ch in s:                 # 글자를 하나씩 꺼내며
        if ch in count:            # 이미 나온 글자면
            count[ch] += 1           # 개수 +1
        else:                        # 처음 나온 글자면
            count[ch] = 1             # 개수를 1로 시작
    return count                # for 밖! (모든 글자를 다 센 뒤에 반환)

# 테스트
print(solution("hello"))    # {'h': 1, 'e': 1, 'l': 2, 'o': 1}
print(solution("banana"))   # {'b': 1, 'a': 3, 'n': 2}
