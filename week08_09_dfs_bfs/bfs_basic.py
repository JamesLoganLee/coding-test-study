# [BFS] 너비 우선 탐색 - 가까운 곳부터 층층이 방문
# 그래프에서 start부터 시작해 모든 노드를 BFS 순서로 방문한 리스트를 반환
# 예: graph={1:[2,3], 2:[1,4], 3:[1,4], 4:[2,3]}, start=1 -> [1, 2, 3, 4]
#
# ═════════════════════════════════════════════════════════════
# [큰 그림] DFS/BFS란?
#   둘 다 '연결된 것들을 하나도 빠짐없이 다 방문하는 방법'이다.
#   예) 미로의 모든 길 탐색, 친구의 친구 찾기, 지도에서 연결된 섬 찾기
#   차이는 '방문하는 순서'뿐이다. (지도는 같은데 산책 방식이 다름)
#
# [그래프란]
#   점(노드)들이 선(간선)으로 연결된 것. 딕셔너리로 표현한다.
#     graph = {1: [2, 3], 2: [1, 4], ...}
#   -> "1번 노드는 2번, 3번과 연결되어 있다"는 뜻 (7주차 딕셔너리 활용)
#
# [BFS 규칙] "가까운 곳부터 차례차례" - 물에 돌을 던지면 파문이
#   동심원으로 퍼지듯, 1걸음 거리를 모두 본 뒤 2걸음 거리로 넘어간다.
#   -> 그래서 '지금 위치에서 갈 수 있는 이웃을 한꺼번에' 대기줄에 넣는다.
#
# [BFS = 큐(Queue)를 쓴다] (7주차 개념 재사용!)
#   큐는 '먼저 넣은 것부터 먼저 꺼내는(FIFO)' 자료구조였다.
#   먼저 발견한 노드부터 처리하다 보면 자연스럽게 '가까운 것부터' 순서가 된다.
#
# [3가지 준비물의 역할 구분 - 헷갈리기 쉬운 부분]
#   visited : "이미 대기줄에 넣었나?" 체크리스트. 큐에 넣는 시점에 기록.
#             (같은 노드가 여러 번 큐에 들어가는 걸 막기 위함)
#   queue   : "다음에 처리할 것들" 대기줄. 아직 처리 안 한 것들.
#   order   : "실제로 처리(방문)한 순서" 최종 결과. 큐에서 꺼내는 시점에 기록.
#   -> visited(예약됨)와 order(실제 완료)는 기록되는 '타이밍'이 다르다!
#
# [실행 흐름 표] graph 위 예시, start=1
#   단계 | 꺼낸node | graph[node] | 이웃검사        | visited      | queue | order
#   1    | 1        | [2,3]       | 2새로움,3새로움 | [1,2,3]      | [2,3] | [1]
#   2    | 2        | [1,4]       | 1있음,4새로움   | [1,2,3,4]    | [3,4] | [1,2]
#   3    | 3        | [1,4]       | 둘다 이미있음   | [1,2,3,4]    | [4]   | [1,2,3]
#   4    | 4        | [2,3]       | 둘다 이미있음   | [1,2,3,4]    | []    | [1,2,3,4]
#   -> 1을 처리할 때 2와 3이 '한꺼번에' 큐에 들어간 것이 핵심.
#      그래서 2 다음 3이 이어서 처리됨 = "가까운 것부터 층층이"
# ═════════════════════════════════════════════════════════════

from collections import deque

def bfs(graph, start):
    visited = [start]           # 방문(예약) 기록 - 시작점부터
    queue = deque([start])      # 대기줄에 시작점 넣기
    order = []                  # 실제 방문 순서를 기록할 리스트

    while queue:                         # 대기줄이 빌 때까지 반복
        node = queue.popleft()            # 먼저 넣은 것부터 꺼내기 (7주차 FIFO)
        order.append(node)                # 방문 순서에 기록(실제 처리 완료)

        for neighbor in graph[node]:       # 지금 노드의 이웃들을 하나씩
            if neighbor not in visited:      # 아직 예약 안 된(안 가본) 이웃이면
                visited.append(neighbor)       # 예약 표시
                queue.append(neighbor)         # 대기줄 뒤에 추가

    return order

# 테스트
graph = {
    1: [2, 3],
    2: [1, 4],
    3: [1, 4],
    4: [2, 3]
}
print(bfs(graph, 1))   # [1, 2, 3, 4]
print(bfs(graph, 2))   # [2, 1, 4, 3]
