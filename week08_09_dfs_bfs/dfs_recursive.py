# [DFS] 깊이 우선 탐색 - 한 방향으로 끝까지 파고들기
# 그래프에서 start부터 시작해 모든 노드를 DFS 순서로 방문한 리스트를 반환
# 예: graph={1:[2,3], 2:[1,4], 3:[1,4], 4:[2,3]}, start=1 -> [1, 2, 4, 3]
#
# ═════════════════════════════════════════════════════════════
# [DFS 규칙] "일단 끝까지 가보고, 막히면 돌아오기"
#   한 방향으로 갈 수 있는 데까지 쭉 가고, 막다른 길이면(더 갈 곳이
#   없으면) 되돌아와서 다른 길로 간다.
#   -> 미로에서 한 손을 벽에 대고 끝까지 따라가는 느낌
#
# [DFS vs BFS 비교]
#   BFS: 큐(대기줄)를 써서 '가까운 것부터' 층층이. 반복문(while)으로 구현.
#   DFS: 재귀를 써서 '한 방향으로' 끝까지. 함수가 자기 자신을 부름.
#
# [재귀(Recursion)란 - 오늘의 핵심 신개념]
#   '함수가 자기 자신을 다시 부르는 것'. 러시아 인형(마트료시카)처럼
#   인형 속에 또 같은 구조의 작은 인형이 들어있는 것과 같다.
#
#   def hello():
#       print("안녕")
#       hello()     <- 함수 안에서 자기 자신을 또 부름!
#
# [왜 DFS엔 재귀가 잘 맞나]
#   "1번을 방문한다" = "1번을 찍고 -> 1번의 이웃에 가서 -> 또 그 이웃을
#   방문한다(또 찍고 또 그 이웃으로...)" 라는 식으로, '방문한다'는 동작
#   안에 또 '방문한다'가 들어있는 구조라서 재귀와 자연스럽게 맞아떨어진다.
#
# [코드 구조 - dfs 함수가 dfs를 또 부름]
#   def dfs(graph, node, visited, order):
#       visited.append(node)          # 방문 표시
#       order.append(node)             # 순서 기록
#       for neighbor in graph[node]:    # 이웃들 확인
#           if neighbor not in visited:   # 안 가본 이웃이면
#               dfs(graph, neighbor, visited, order)  # <- 재귀 호출!
#
# [손으로 따라가기 - 재귀가 '안으로 들어갔다 되돌아 나오는' 과정]
#   dfs(1) 호출
#     -> 1 방문, order=[1]
#     -> 1의 이웃[2,3] 중 2가 안 가봤으니
#       -> dfs(2) 호출 (여기서 잠깐 멈추고 안으로 들어감!)
#           -> 2 방문, order=[1,2]
#           -> 2의 이웃[1,4] 중 4가 안 가봤으니
#             -> dfs(4) 호출 (또 안으로!)
#                 -> 4 방문, order=[1,2,4]
#                 -> 4의 이웃[2,3] 중 3이 안 가봤으니
#                   -> dfs(3) 호출 (또 안으로!)
#                       -> 3 방문, order=[1,2,4,3]
#                       -> 3의 이웃[1,4] 둘 다 이미 있음 -> 더 갈 곳 없음
#                       -> dfs(3) 끝, 밖으로 되돌아감
#                 -> dfs(4)도 더 볼 이웃 없음 -> 끝, 되돌아감
#           -> dfs(2)도 더 볼 이웃 없음 -> 끝, 되돌아감
#     -> 1의 남은 이웃 3 확인 -> 이미 visited에 있어서 스킵
#     -> dfs(1) 끝
#   최종 결과: [1, 2, 4, 3]
#
# [핵심 포인트] "되돌아간다"는 것의 의미
#   dfs(3)이 끝나면 -> 그걸 불렀던 dfs(4)로 자동으로 돌아가고
#   dfs(4)가 끝나면 -> 그걸 불렀던 dfs(2)로 돌아가고... 이렇게 재귀는
#   안으로 들어갔다가 자동으로 거슬러 올라온다.
#   이것이 "막히면 되돌아온다"는 DFS의 특징을 그대로 구현해준다.
#
# [BFS와 결정적 차이 - 이웃을 보는 방식]
#   BFS: 이웃을 한꺼번에 큐에 담아두고, 나중에 순서대로 꺼내 처리 (동시에 예약)
#   DFS: 이웃 하나를 발견하면 '즉시 그 안으로' 들어가 버림 (바로 파고듦)
#   -> 그래서 BFS는 [1,2,3,4](2,3을 먼저 예약), DFS는 [1,2,4,3](2를 만나자마자 파고듦)
# ═════════════════════════════════════════════════════════════

def dfs(graph, node, visited, order):
    visited.append(node)               # 지금 노드를 방문 표시
    order.append(node)                  # 방문 순서에 기록

    for neighbor in graph[node]:         # 지금 노드의 이웃들을 하나씩
        if neighbor not in visited:        # 아직 안 가봤으면
            dfs(graph, neighbor, visited, order)   # 그 이웃으로 즉시 파고들기(재귀)

    return order

# 테스트
graph = {
    1: [2, 3],
    2: [1, 4],
    3: [1, 4],
    4: [2, 3]
}
print(dfs(graph, 1, [], []))   # [1, 2, 4, 3]
print(dfs(graph, 2, [], []))   # [2, 1, 3, 4]
